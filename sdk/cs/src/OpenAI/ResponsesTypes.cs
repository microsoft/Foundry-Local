/// <summary>Tool choice — either a known string value or a specific function.</summary>
[JsonConverter(typeof(ToolChoiceConverter))]
public class ToolChoice
{
    public ToolChoiceValue? Value { get; set; }

    public SpecificToolChoice? Specific { get; set; }

    public static implicit operator ToolChoice(ToolChoiceValue value) => new() { Value = value };

    public static ToolChoice ForFunction(string name) => new() { Specific = new SpecificToolChoice { Name = name } };
}

public class ToolChoiceConverter : JsonConverter<ToolChoice>
{
    public override ToolChoice? Read(ref Utf8JsonReader reader, Type typeToConvert, JsonSerializerOptions options)
    {
        if (reader.TokenType == JsonTokenType.String)
        {
            var str = reader.GetString();
            return str switch
            {
                "auto" => new ToolChoice { Value = ToolChoiceValue.Auto },
                "none" => new ToolChoice { Value = ToolChoiceValue.None },
                "required" => new ToolChoice { Value = ToolChoiceValue.Required },
                _ => throw new JsonException($"Unknown tool_choice value: {str}"),
            };
        }

        if (reader.TokenType == JsonTokenType.StartObject)
        {
            using var doc = JsonDocument.ParseValue(ref reader);
            var json = doc.RootElement.GetRawText();
            var specific = JsonSerializer.Deserialize(json, ResponsesSerializationContext.Default.SpecificToolChoice);
            return new ToolChoice { Specific = specific };
        }

        if (reader.TokenType == JsonTokenType.Null)
        {
            return null;
        }

        throw new JsonException("tool_choice must be a string or object.");
    }

    public override void Write(Utf8JsonWriter writer, ToolChoice value, JsonSerializerOptions options)
    {
        if (value.Value.HasValue)
        {
            var str = value.Value.Value switch
            {
                ToolChoiceValue.Auto => "auto",
                ToolChoiceValue.None => "none",
                ToolChoiceValue.Required => "required",
                _ => throw new JsonException($"Unknown ToolChoiceValue: {value.Value}"),
            };
            writer.WriteStringValue(str);
        }
        else if (value.Specific != null)
        {
            JsonSerializer.Serialize(writer, value.Specific, ResponsesSerializationContext.Default.SpecificToolChoice);
        }
        else
        {
            writer.WriteNullValue();
        }
    }
}

