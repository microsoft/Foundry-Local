/// <summary>Image content provided as input.</summary>
public sealed class InputImageContent : ContentPart
{
    /// <inheritdoc/>
    [JsonIgnore]
    public override string Kind => "input_image";

    /// <summary>Public URL of an image.</summary>
    [JsonPropertyName("image_url")]
    [JsonIgnore(Condition = JsonIgnoreCondition.WhenWritingNull)]
    public string? ImageUrl { get; set; }

    /// <summary>Base64-encoded image data.</summary>
    [JsonPropertyName("image_data")]
    [JsonIgnore(Condition = JsonIgnoreCondition.WhenWritingNull)]
    public string? ImageData { get; set; }

    /// <summary>MIME type of the image (e.g., <c>image/png</c>, <c>image/jpeg</c>). Optional — the server will
    /// infer the type from the data when omitted.</summary>
    [JsonPropertyName("media_type")]
    [JsonIgnore(Condition = JsonIgnoreCondition.WhenWritingNull)]
    public string? MediaType { get; set; }

    /// <summary>Detail level for image processing: "low", "high", or "auto".</summary>
    [JsonPropertyName("detail")]
    [JsonIgnore(Condition = JsonIgnoreCondition.WhenWritingNull)]
    public string? Detail { get; set; }

    /// <summary>Validates that exactly one of <see cref="ImageUrl"/> or <see cref="ImageData"/> is set.
    /// Called by the client before serialization to surface caller errors close to the call site rather than
    /// returning a server-side 4xx. The factory methods (<see cref="FromFile"/>, <see cref="FromUrl"/>,
    /// <see cref="FromBytes"/>) already produce valid instances; this guards against direct property mutation.</summary>
    /// <exception cref="InvalidOperationException">If both or neither of <see cref="ImageUrl"/> / <see cref="ImageData"/> are set.</exception>
    public void Validate()
    {
        var hasUrl = !string.IsNullOrEmpty(ImageUrl);
        var hasData = !string.IsNullOrEmpty(ImageData);
        if (hasUrl == hasData)
        {
            throw new InvalidOperationException(
                "InputImageContent requires exactly one of ImageUrl or ImageData to be set.");
        }
    }

    /// <summary>Load an image from a local file; base64 encode and detect media type from extension.</summary>
    /// <exception cref="ArgumentException">If <paramref name="path"/> is null/empty.</exception>
    /// <exception cref="FileNotFoundException">If the file does not exist at <paramref name="path"/>.</exception>
    public static InputImageContent FromFile(string path, string? detail = null)
    {
        if (string.IsNullOrWhiteSpace(path))
        {
            throw new ArgumentException("path must be non-empty.", nameof(path));
        }

        if (!File.Exists(path))
        {
            throw new FileNotFoundException($"Image file not found: {path}", path);
        }

        var bytes = File.ReadAllBytes(path);
        var mediaType = DetectMediaType(path);
        return FromBytes(bytes, mediaType, detail);
    }

    /// <summary>Create an image content referencing a URL. <paramref name="mediaType"/> is optional;
    /// when null the server will infer it.</summary>
    public static InputImageContent FromUrl(string url, string? detail = null, string? mediaType = null)
    {
        if (string.IsNullOrWhiteSpace(url))
        {
            throw new ArgumentException("url must be non-empty.", nameof(url));
        }

        return new InputImageContent
        {
            ImageUrl = url,
            MediaType = mediaType,
            Detail = detail,
        };
    }

    /// <summary>Create an image content from raw bytes. <paramref name="mediaType"/> is optional;
    /// when null the server will infer it from the data.</summary>
    public static InputImageContent FromBytes(byte[] data, string? mediaType = null, string? detail = null)
    {
        if (data == null || data.Length == 0)
        {
            throw new ArgumentException("data must be non-empty.", nameof(data));
        }

        return new InputImageContent
        {
            ImageData = Convert.ToBase64String(data),
            MediaType = mediaType,
            Detail = detail,
        };
    }

    private static string? DetectMediaType(string path)
    {
        var ext = Path.GetExtension(path).ToLowerInvariant();
        return ext switch
        {
            ".png" => "image/png",
            ".jpg" or ".jpeg" => "image/jpeg",
            ".gif" => "image/gif",
            ".webp" => "image/webp",
            ".bmp" => "image/bmp",
            _ => null,
        };
    }
}

