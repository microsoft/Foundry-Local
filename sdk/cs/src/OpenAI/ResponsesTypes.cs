    public ResponseUsage? Usage { get; set; }

    [JsonPropertyName("max_output_tokens")]
    public int? MaxOutputTokens { get; set; }

    [JsonPropertyName("store")]
    public bool Store { get; set; }

    [JsonPropertyName("metadata")]
    public Dictionary<string, string>? Metadata { get; set; }

    [JsonPropertyName("user")]
    public string? User { get; set; }

    /// <summary>
    /// Convenience: concatenates text from assistant <see cref="MessageItem"/> instances in <see cref="Output"/>.
    /// Returns empty string if no assistant message is found.
    /// </summary>
    [JsonIgnore]
    public string OutputText
    {
        get
        {
            var sb = new System.Text.StringBuilder();
            foreach (var item in Output)
            {
                if (item is MessageItem msg && msg.Role == MessageRole.Assistant)
                {
                    sb.Append(msg.Content.GetText());
                }
            }

            return sb.ToString();
        }
    }
}

