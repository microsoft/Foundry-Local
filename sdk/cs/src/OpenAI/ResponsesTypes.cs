/// <summary>
/// Represents message content — either a simple string or an array of content parts.
/// Use implicit conversions or factory methods for convenience.
/// </summary>
[JsonConverter(typeof(MessageContentConverter))]
public class MessageContent
{
    /// <summary>The text content if this is a simple string message.</summary>
    public string? Text { get; set; }

    /// <summary>The content parts if this is a structured multimodal message.</summary>
    public List<ContentPart>? Parts { get; set; }

    public static implicit operator MessageContent(string text) => new() { Text = text };

    public static implicit operator MessageContent(List<ContentPart> parts) => new() { Parts = parts };

    public static MessageContent FromText(string text) => new() { Text = text };

    public static MessageContent FromParts(params ContentPart[] parts) => new() { Parts = [.. parts] };

    /// <summary>Get the concatenated text representation.</summary>
    public string GetText()
    {
        if (Text != null)
        {
            return Text;
        }

        if (Parts == null)
        {
            return string.Empty;
        }

        var sb = new System.Text.StringBuilder();
        foreach (var part in Parts)
        {
            if (part is ITextContent t)
            {
                sb.Append(t.Text);
            }
        }

        return sb.ToString();
    }
}

/// <summary>JSON converter for <see cref="MessageContent"/> supporting string or array form.</summary>
public class MessageContentConverter : JsonConverter<MessageContent>
{
    public override MessageContent? Read(ref Utf8JsonReader reader, Type typeToConvert, JsonSerializerOptions options)
    {
        if (reader.TokenType == JsonTokenType.String)
        {
            return new MessageContent { Text = reader.GetString() };
        }

        if (reader.TokenType == JsonTokenType.StartArray)
        {
            var parts = new List<ContentPart>();
            while (reader.Read() && reader.TokenType != JsonTokenType.EndArray)
            {
                parts.Add(ReadContentPart(ref reader));
            }

            return new MessageContent { Parts = parts };
        }

        if (reader.TokenType == JsonTokenType.Null)
        {
            return null;
        }

        throw new JsonException("MessageContent must be a string or array.");
    }

    private static ContentPart ReadContentPart(ref Utf8JsonReader reader)
    {
        if (reader.TokenType != JsonTokenType.StartObject)
        {
            throw new JsonException($"Expected ContentPart object, found {reader.TokenType}.");
        }

        using var doc = JsonDocument.ParseValue(ref reader);
        var root = doc.RootElement;
        if (!root.TryGetProperty("type", out var typeEl))
        {
            throw new JsonException("ContentPart must have a 'type' property.");
        }

        var type = typeEl.GetString();
        var json = root.GetRawText();
        var ctx = ResponsesSerializationContext.Default;

        ContentPart? result = type switch
        {
            "input_text" => JsonSerializer.Deserialize(json, ctx.InputTextContent),
            "output_text" => JsonSerializer.Deserialize(json, ctx.OutputTextContent),
            "refusal" => JsonSerializer.Deserialize(json, ctx.RefusalContent),
            "input_image" => JsonSerializer.Deserialize(json, ctx.InputImageContent),
            "input_file" => JsonSerializer.Deserialize(json, ctx.InputFileContent),
            _ => throw new JsonException($"Unsupported content part type: '{type}'."),
        };

        return result ?? throw new JsonException($"Failed to deserialize content part of type '{type}'.");
    }

    public override void Write(Utf8JsonWriter writer, MessageContent value, JsonSerializerOptions options)
    {
        if (value.Text != null)
        {
            writer.WriteStringValue(value.Text);
        }
        else if (value.Parts != null)
        {
            writer.WriteStartArray();
            foreach (var part in value.Parts)
            {
                JsonSerializer.Serialize(writer, part, ResponsesSerializationContext.Default.ContentPart);
            }

            writer.WriteEndArray();
        }
        else
        {
            writer.WriteStringValue(string.Empty);
        }
    }
}

