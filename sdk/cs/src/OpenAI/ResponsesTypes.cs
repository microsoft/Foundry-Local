/// <summary>
/// Represents the input field of a Response create request.
/// Supports either a simple string or an array of structured items.
/// </summary>
[JsonConverter(typeof(ResponseInputConverter))]
public class ResponseInput
{
    public string? Text { get; set; }

    public List<ResponseItem>? Items { get; set; }

    public static implicit operator ResponseInput(string text) => new() { Text = text };

    public static implicit operator ResponseInput(List<ResponseItem> items) => new() { Items = items };
}

public class ResponseInputConverter : JsonConverter<ResponseInput>
{
    public override ResponseInput? Read(ref Utf8JsonReader reader, Type typeToConvert, JsonSerializerOptions options)
    {
        if (reader.TokenType == JsonTokenType.String)
        {
            return new ResponseInput { Text = reader.GetString() };
        }

        if (reader.TokenType == JsonTokenType.StartArray)
        {
            var items = new List<ResponseItem>();
            while (reader.Read() && reader.TokenType != JsonTokenType.EndArray)
            {
                var item = ReadResponseItem(ref reader);
                if (item != null)
                {
                    items.Add(item);
                }
            }

            return new ResponseInput { Items = items };
        }

        if (reader.TokenType == JsonTokenType.Null)
        {
            return null;
        }

        throw new JsonException("Input must be a string or array of items.");
    }

    private static ResponseItem? ReadResponseItem(ref Utf8JsonReader reader)
    {
        if (reader.TokenType != JsonTokenType.StartObject)
        {
            reader.Skip();
            return null;
        }

        using var doc = JsonDocument.ParseValue(ref reader);
        var root = doc.RootElement;
        if (!root.TryGetProperty("type", out var typeEl))
        {
            return null;
        }

        var type = typeEl.GetString();
        var json = root.GetRawText();
        var ctx = ResponsesSerializationContext.Default;

        return type switch
        {
            "message" => JsonSerializer.Deserialize(json, ctx.MessageItem),
            "function_call" => JsonSerializer.Deserialize(json, ctx.FunctionCallItem),
            "function_call_output" => JsonSerializer.Deserialize(json, ctx.FunctionCallOutputItem),
            "reasoning" => JsonSerializer.Deserialize(json, ctx.ReasoningItem),
            "item_reference" => JsonSerializer.Deserialize(json, ctx.ItemReference),
            _ => null,
        };
    }

    public override void Write(Utf8JsonWriter writer, ResponseInput value, JsonSerializerOptions options)
    {
        if (value.Text != null)
        {
            writer.WriteStringValue(value.Text);
        }
        else if (value.Items != null)
        {
            var ctx = ResponsesSerializationContext.Default;
            writer.WriteStartArray();
            foreach (var item in value.Items)
            {
                JsonSerializer.Serialize(writer, item, ctx.ResponseItem);
            }

            writer.WriteEndArray();
        }
        else
        {
            writer.WriteNullValue();
        }
    }
}

